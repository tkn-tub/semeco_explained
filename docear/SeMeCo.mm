<node TEXT="Individual-based" ID="ID_22731700" CREATED="1741254972096" MODIFIED="1741255566447" MOVED="1741256362049"><richcontent TYPE="NOTE">

<html>
  <head>
    
  </head>
  <body>
    <ul>
      <li>
        Individual-based (also known as agent-based) models (IbMs) explicitly treat each individual cell as a discrete independent entity that interacts with other individuals and with its continuous environment.
      </li>
      <li>
        These models allow the introduction of individual variability (e.g., in growth rates, substrate uptake and secretions rates, cell mass, cell volume, etc.).
      </li>
      <li>
        This modeling formalism is a bottom-up approach where the dynamics and function of the whole system is governed by that of individual cells in their pursuit of optimal fitness.
      </li>
      <li>
        While the biomass spreading is modeled using the discrete individual-based approach (where cells are modeled as spheres spreading only when they get too close to each other), changes in concentration of soluble substrates in the continuous environment can be modeled using reaction-diffusion equations.
      </li>
      <li>
        Therefore, in contrast to previously described PDE models, here reaction-diffusion models are used only to follow the dynamics of the shared compounds rather than microbial biomass.
      </li>
      <li>
        The discrete and continuous models are then integrated numerically using the so-called &#8220;hybrid Eulerian&#8211;Lagrangian approach&#8221;
      </li>
      <li>
        This spatial segregation allows cooperative cells to preferentially interact with other cooperative cells thereby avoiding exploitation by cheaters and favoring the evolution cooperation.
      </li>
    </ul>
  </body>
</html>
